def T_UI(lang: str, key: str) -> str:
    """Return translated string for key in lang ('en'|'zh').

    Falls back to the EN_LABELS value, then the key itself.
    """
    if lang == "zh":
        return ZH.get(key, EN_LABELS.get(key, key))
    return EN_LABELS.get(key, key)


